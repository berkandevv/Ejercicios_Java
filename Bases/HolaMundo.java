/**
 * Programa que recibe una frase y la muestra por pantalla.
 * El método `muestraPorPantalla` toma un String como parámetro 
 * y lo retorna para que se imprima por consola.
 */

public class HolaMundo {

    static String muestraPorPAntalla(String frase) {
        return frase;
    }

    public static void main(String[] args) {

        // Llamamos el método que hemos creado
        System.out.println(muestraPorPAntalla("Hola Mundo!"));
    }
}
